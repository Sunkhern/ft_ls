#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list	*tmp;

	tmp = (t_list *)malloc(sizeof(t_list));
	if (tmp == NULL)
		return (NULL);
	if (content == NULL)
	{
		tmp->content = NULL;
		tmp->content_size = 0;
	}
	else
	{
		tmp->content = malloc(sizeof(content));
		if (tmp->content == NULL)
			return (NULL);
		ft_memcpy((tmp->content), content, sizeof(content));
		tmp->content_size = content_size;
	}
	tmp->next = NULL;
	return (tmp);
}
