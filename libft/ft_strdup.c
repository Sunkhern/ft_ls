#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*res;

	i = ft_strlen(str);
	res = malloc(sizeof(char) * i + 1);
	i = 0;
	while (str[i] != '\0')
	{
		res[i] = str[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
