#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int		i;
	int		l;

	i = 0;
	l = ft_strlen(str);
	if ((char)c == '\0')
		return ((char *)&str[l]);
	while (str[i] != '\0')
	{
		if (str[i] == (char)c)
			return ((char *)&str[i]);
		i++;
	}
	return (NULL);
}
